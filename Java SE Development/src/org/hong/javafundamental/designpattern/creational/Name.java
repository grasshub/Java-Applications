package org.hong.javafundamental.designpattern.creational;

class Name {
	
	//a simple class to take a string apart into two names
	protected String lastName; //store last name here
	protected String firstName; //store first name here
	
	public String getFirstName() {
		return firstName; //return first name
	}
	
	public String getLastName() {
		return lastName; //return last name
	}
}
